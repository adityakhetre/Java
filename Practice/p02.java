//access specifiers

class Demo{

	int x = 10;			//instance variable of type default access specifire		//it can access in same folder

	private int y = 20;		//instance variable of type private access specifire		//it can access in only class

	public int z = 30;		//instance variable of type public access specifire		//it can access anywhere need to import package 

	protected int a = 40;		//instance variable of type protected access specifire		

	void fun(){

		System.out.println("java");
	}
}





