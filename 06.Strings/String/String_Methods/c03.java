/* 
 * method3- public char charAt(int index);
 *
 * description-
 * -it returns character located at specified index within the given String
 *
 * parameters- Integer (index)
 * returntype- character
 */

class CharAtDemo{

        public static void main(String[] args){

                String s1 = "Core2Web";

                System.out.println(s1.charAt(0));		//prints char at that index 	//index starts from 0

                System.out.println(s1.charAt(4));

                System.out.println(s1.charAt(7));
        }
}
